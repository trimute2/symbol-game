function platform(map){
	this.platforms = [];
	this.pos = 0;
	for (var h = 0; h <= 10; h++) {
		for (var w = 0; w <= 9; w++) {
			if(map[h][w] === '_'){
				this.platforms[this.pos] = [w*5,h*5];
				this.pos++;
			}
		}
	}
	return this.platforms;
}
